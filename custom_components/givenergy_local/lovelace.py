def _battery_view(bats: list[str]) -> str:
    sections = "\n".join(_battery_section(s) for s in bats)
    return f"""\
  # ── Battery ───────────────────────────────────────────────────────────────
  - title: Battery
    path: battery
    type: sections
    icon: mdi:battery-high

    sections:
{sections}"""


def _battery_section(serial: str) -> str:
    pack = _bat_entity_rows(
        serial,
        [
            ("soc", "SOC"),
            ("voltage", "Voltage"),
            ("temperature_max", "Temp Max"),
            ("temperature_min", "Temp Min"),
            ("bms_mosfet_temperature", "BMS MOSFET Temp"),
            None,
            ("charge_cycles", "Charge Cycles"),
            ("remaining_capacity", "Remaining Capacity"),
            ("calibrated_capacity", "Calibrated Capacity"),
            ("design_capacity", "Design Capacity"),
            ("cell_voltages_sum", "Cell Voltages Sum"),
            ("cell_count", "Cell Count"),
        ],
    )
    cells = _bat_entity_rows(serial, [(f"cell_{i}_voltage", f"Cell {i}") for i in range(1, 17)])
    temps = _bat_entity_rows(
        serial,
        [
            ("cells_1_4_temperature", "Cells 1–4"),
            ("cells_5_8_temperature", "Cells 5–8"),
            ("cells_9_12_temperature", "Cells 9–12"),
            ("cells_13_16_temperature", "Cells 13–16"),
        ],
    )

    return f"""\
      # ── {serial} ──
      - cards:
          - type: gauge
            entity: {_b(serial, "soc")}
            name: {serial}
            min: 0
            max: 100
            needle: true
            severity:
              red: 0
              yellow: 20
              green: 40

          - type: entities
            title: Pack Details
            entities:
{pack}

          - type: entities
            title: Cell Voltages
            entities:
{cells}

          - type: entities
            title: Cell Temperatures
            entities:
{temps}
"""


