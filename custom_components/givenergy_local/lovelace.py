"""Generate a Lovelace dashboard YAML for the GivEnergy Local integration."""

from __future__ import annotations


def generate_dashboard(inv: str, bats: list[str]) -> str:
    """Return a complete Lovelace dashboard YAML string.

    Args:
        inv:  Inverter serial number (lowercase).
        bats: Battery serial number(s) (lowercase).
    """
    views = "\n".join(
        [
            _overview_view(inv),
            _energy_view(inv),
            _battery_view(bats),
            _controls_view(inv),
            _diagnostics_view(inv),
        ]
    )
    return (
        f"# Generated by GivEnergy Local — givenergy_local.generate_dashboard\n"
        f"# Inverter : {inv}\n"
        f"# Batteries: {', '.join(bats)}\n"
        f"#\n"
        f"# Required custom cards (install via HACS → Frontend):\n"
        f"#   - power-flow-card-plus  (flixlix/power-flow-card-plus)\n"
        f"#   - mini-graph-card  (kalkih/mini-graph-card)\n"
        f"#   - apexcharts-card  (RomRider/apexcharts-card)\n"
        f"\n"
        f"title: GivEnergy\n"
        f"views:\n"
        f"\n"
        f"{views}\n"
    )


